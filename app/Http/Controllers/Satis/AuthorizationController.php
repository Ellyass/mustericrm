     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $data['users'] = User::where('id',$id)
            ->first();

        $users = User::orderBy('id','desc')
            ->get();


        return view('backend.satis.authorization.edit')
            ->with('users',$users)
